#include "3-calc.h"

/**
 * op_add - add two int
 *
 * @a: int1
 * @b: int2
 *
 * Return: sum of the two ints
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub two int
 *
 * @a: int1
 * @b: int2
 *
 * Return: diff of the two ints
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul two int
 *
 * @a: int1
 * @b: int2
 *
 * Return: product of the two ints
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div two int
 *
 * @a: int1
 * @b: int2
 *
 * Return: division of the two ints
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod two int
 *
 * @a: int1
 * @b: int2
 *
 * Return: remainder of the division of the two ints
 */
int op_mod(int a, int b)
{
	return (a % b);
}
